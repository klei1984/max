/// Helper macro to define RGBA palette entries with opaque alpha (255)
#define RGBA_ENTRY(r, g, b) {r, g, b, 255}

/// Number of entries in installer palettes
inline constexpr uint32_t PALETTE_RGBA_SIZE = 256u;

/**
 * \struct PaletteRGBA
 * \brief RGBA8888 color representation for palette entries with native endianness.
 *
 * This structure stores pre-multiplied 8-bit RGBA values suitable for direct use in rendering. The memory layout
 * matches the native byte order of the target platform, ensuring efficient access without endianness conversions.
 */
