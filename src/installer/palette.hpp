/**
 * \brief Retrieves a single RGBA8888 color from the specified palette with bounds checking.
 *
 * \param palette Reference to palette array.
 * \param palette_index Palette index.
 * \return RGBA8888 color structure. Returns transparent black {0,0,0,0} if index >= PALETTE_RGBA_SIZE.
 */
inline PaletteRGBA Palette_GetRGBA(const PaletteRGBA (&palette)[PALETTE_RGBA_SIZE], uint32_t palette_index) noexcept {
    if (palette_index >= PALETTE_RGBA_SIZE) {
        return {0, 0, 0, 0};
    }

    return palette[palette_index];
}

